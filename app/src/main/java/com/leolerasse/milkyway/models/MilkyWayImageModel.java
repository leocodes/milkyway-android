package com.leolerasse.milkyway.models;

public class MilkyWayImageModel {
    public String center;
    public String title;
    public String description;
    public String date;
    public String image;

    public MilkyWayImageModel(String center, String title, String description, String date, String image) {
        this.center = center;
        this.title = title;
        this.description = description;
        this.date = date;
        this.image = image;
    }
}
